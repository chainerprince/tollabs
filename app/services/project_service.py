# ── Helpers ───────────────────────────────────────────────────────

def _slug(model_id: str) -> str:
    return model_id.replace("/", "--").replace(" ", "-").lower()


def _project_dir(user_id: int, slug: str) -> Path:
    return WORKSPACE_ROOT / str(user_id) / "projects" / slug


# ── CRUD ──────────────────────────────────────────────────────────

def create_project(
    user_id: int,
    model_id: str,
    model_name: str,
    task: str,
    tags: list[str],
    parameter_count: str = "",
    description: str = "",
) -> dict[str, Any]:
    """Create a new research project with auto-generated notebook cells."""
    slug = _slug(model_id)
    pdir = _project_dir(user_id, slug)
    pdir.mkdir(parents=True, exist_ok=True)

    project_id = uuid.uuid4().hex[:12]
    cells = _generate_cells(model_id, model_name, task, tags, parameter_count, description)

    meta: dict[str, Any] = {
        "id": project_id,
        "slug": slug,
        "model_id": model_id,
        "model_name": model_name,
        "task": task,
        "tags": tags,
        "parameter_count": parameter_count,
        "description": description,
        "gpu_tier": "t4",
        "created_at": datetime.now(timezone.utc).isoformat(),
        "cells": cells,
    }

    # Write project.json
    (pdir / "project.json").write_text(json.dumps(meta, indent=2))

    # Write individual .py files for the folder view
    for i, cell in enumerate(cells):
        safe = cell["title"].lower().replace(" ", "_").replace("&", "and").replace("/", "_")
        fname = f"{i:02d}_{safe}.py"
        (pdir / fname).write_text(cell["code"])

    # Write requirements.txt
    reqs = _requirements_for_task(task)
    (pdir / "requirements.txt").write_text(reqs)

    # Write README
    readme = f"# {model_name}\n\n**Model:** `{model_id}`  \n**Task:** {task}  \n**Parameters:** {parameter_count}\n\n{description}\n\n---\nAuto-generated by TOLLABS Editor.\n"
    (pdir / "README.md").write_text(readme)

    return meta


def create_project_from_code(
    user_id: int,
    name: str,
    code: str,
    description: str = "",
) -> dict[str, Any]:
    """Create a project from raw strategy code (Studio → Editor flow)."""
    slug = name.lower().replace(" ", "-").replace("/", "-")[:60]
    slug = _slug(slug)
    pdir = _project_dir(user_id, slug)
    pdir.mkdir(parents=True, exist_ok=True)

    project_id = uuid.uuid4().hex[:12]
    cells = [
        {"title": "Strategy", "code": code},
    ]

    meta: dict[str, Any] = {
        "id": project_id,
        "slug": slug,
        "model_id": "",
        "model_name": name,
        "task": "strategy",
        "tags": ["studio", "strategy"],
        "parameter_count": "",
        "description": description,
        "gpu_tier": "cpu",
        "created_at": datetime.now(timezone.utc).isoformat(),
        "cells": cells,
    }

    (pdir / "project.json").write_text(json.dumps(meta, indent=2))
    (pdir / "00_strategy.py").write_text(code)
    (pdir / "requirements.txt").write_text("numpy>=1.24.0\npandas>=2.0.0\n")
    (pdir / "README.md").write_text(
        f"# {name}\n\n{description}\n\n---\nCreated from TOLLABS Studio.\n"
    )

    return meta


def list_projects(user_id: int) -> list[dict[str, Any]]:
    """List all projects for a user (lightweight — no cells)."""
    projects_dir = WORKSPACE_ROOT / str(user_id) / "projects"
    if not projects_dir.exists():
        return []

    results = []
    for d in sorted(projects_dir.iterdir()):
        meta_path = d / "project.json"
        if not meta_path.exists():
            continue
        meta = json.loads(meta_path.read_text())
        # Return lightweight summary (no cells)
        results.append({
            "id": meta.get("id", ""),
            "slug": meta.get("slug", d.name),
            "model_id": meta.get("model_id", ""),
            "model_name": meta.get("model_name", d.name),
            "task": meta.get("task", ""),
            "tags": meta.get("tags", []),
            "parameter_count": meta.get("parameter_count", ""),
            "gpu_tier": meta.get("gpu_tier", "t4"),
            "created_at": meta.get("created_at", ""),
            "file_count": len([f for f in d.iterdir() if f.is_file() and f.name != "project.json"]),
        })
    return results


def get_project(user_id: int, slug: str) -> dict[str, Any] | None:
    """Get full project details including cells and files."""
    pdir = _project_dir(user_id, slug)
    meta_path = pdir / "project.json"
    if not meta_path.exists():
        return None

    meta = json.loads(meta_path.read_text())

    # Enumerate files
    files = []
    for f in sorted(pdir.iterdir()):
        if f.is_file() and f.name != "project.json":
            files.append({
                "name": f.name,
                "size": f.stat().st_size,
                "type": f.suffix.lstrip(".") or "file",
            })
    meta["files"] = files
    return meta


def get_project_file(user_id: int, slug: str, filename: str) -> str | None:
    """Read a single file from a project."""
    fpath = _project_dir(user_id, slug) / filename
    if not fpath.exists():
        return None
    return fpath.read_text()


def save_project_cells(user_id: int, slug: str, cells: list[dict]) -> bool:
    """Save updated cells back to the project."""
    pdir = _project_dir(user_id, slug)
    meta_path = pdir / "project.json"
    if not meta_path.exists():
        return False

    meta = json.loads(meta_path.read_text())
    meta["cells"] = cells
    (pdir / "project.json").write_text(json.dumps(meta, indent=2))
    return True


def update_gpu_tier(user_id: int, slug: str, gpu_tier: str) -> bool:
    """Update the selected GPU tier for a project."""
    pdir = _project_dir(user_id, slug)
    meta_path = pdir / "project.json"
    if not meta_path.exists():
        return False

    meta = json.loads(meta_path.read_text())
    meta["gpu_tier"] = gpu_tier
    (pdir / "project.json").write_text(json.dumps(meta, indent=2))
    return True


# ── Code generation ──────────────────────────────────────────────

def _generate_cells(
    model_id: str,
    model_name: str,
    task: str,
    tags: list[str],
    parameter_count: str,
    description: str,
) -> list[dict[str, str]]:
    """Generate notebook cells appropriate for the model's task."""
    if task in ("sentiment-analysis", "text-classification"):
        return _cells_classification(model_id, model_name, task, tags, parameter_count, description)
    elif task == "text-generation":
        return _cells_generation(model_id, model_name, tags, parameter_count, description)
    elif task == "time-series-forecasting":
        return _cells_timeseries(model_id, model_name, tags, parameter_count, description)
    else:
        return _cells_generic(model_id, model_name, task, tags, parameter_count, description)


