def _requirements_for_task(task: str) -> str:
    """Generate requirements.txt for the task."""
    base = "transformers>=4.36.0\ntorch>=2.1.0\ndatasets>=2.16.0\naccelerate>=0.25.0\npeft>=0.7.0\n"
    if task == "time-series-forecasting":
        base += "chronos-forecasting>=1.0.0\npandas>=2.0.0\nmatplotlib>=3.7.0\n"
    elif task in ("sentiment-analysis", "text-classification"):
        base += "scikit-learn>=1.3.0\n"
    return base
